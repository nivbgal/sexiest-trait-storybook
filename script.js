const state = {
  currentScene: 0
};

const scenePlayer = document.getElementById("scenePlayer");
const sceneBackdrop = document.getElementById("sceneBackdrop");
const sceneImage = document.getElementById("sceneImage");
const sceneCount = document.getElementById("sceneCount");
const sceneDots = document.getElementById("sceneDots");
const sceneKicker = document.getElementById("sceneKicker");
const storyLines = document.getElementById("storyLines");
const storyEnd = document.getElementById("storyEnd");
const prevButton = document.getElementById("prevButton");
const actionButton = document.getElementById("actionButton");

function currentSceneData() {
  return scenes[state.currentScene];
}

function isLastScene() {
  return state.currentScene === scenes.length - 1;
}

function renderDots() {
  sceneDots.innerHTML = "";

  scenes.forEach((scene, index) => {
    const dot = document.createElement("button");
    dot.type = "button";
    dot.className = "scene-dot";
    dot.setAttribute("aria-label", `לעמוד ${index + 1}: ${scene.title}`);

    if (index === state.currentScene) {
      dot.classList.add("is-active");
      dot.setAttribute("aria-current", "page");
    }

    dot.addEventListener("click", () => setScene(index));
    sceneDots.appendChild(dot);
  });
}

function renderLines() {
  const scene = currentSceneData();

  storyLines.innerHTML = "";

  scene.lines.forEach((line) => {
    const paragraph = document.createElement("p");
    paragraph.className = "story-line";
    paragraph.textContent = line;
    storyLines.appendChild(paragraph);
  });
}

function actionLabel() {
  return isLastScene() ? "סוף הסיפור" : "העמוד הבא";
}

function animateSceneChange() {
  scenePlayer.classList.remove("is-changing");
  void scenePlayer.offsetWidth;
  scenePlayer.classList.add("is-changing");
}

function renderScene() {
  const scene = currentSceneData();

  sceneBackdrop.style.backgroundImage = `url("${scene.image}")`;
  sceneImage.src = scene.image;
  sceneImage.alt = scene.alt;
  sceneCount.textContent = "";
  sceneCount.style.display = "none";
  sceneKicker.textContent = `${state.currentScene + 1}/${scenes.length}`;
  actionButton.textContent = actionLabel();
  prevButton.disabled = state.currentScene === 0;
  actionButton.disabled = isLastScene();
  storyEnd.hidden = !isLastScene();
  storyEnd.style.display = isLastScene() ? "inline-flex" : "none";

  renderLines();
  renderDots();
  animateSceneChange();
}

function setScene(index) {
  if (index < 0 || index >= scenes.length || index === state.currentScene) {
    return;
  }

  state.currentScene = index;
  renderScene();
}

function goToNextScene() {
  if (isLastScene()) {
    return;
  }

  setScene(state.currentScene + 1);
}

prevButton.addEventListener("click", () => setScene(state.currentScene - 1));
actionButton.addEventListener("click", goToNextScene);

window.addEventListener("keydown", (event) => {
  if (event.key === "ArrowLeft" || event.key === " " || event.key === "Enter") {
    event.preventDefault();
    goToNextScene();
  }

  if (event.key === "ArrowRight") {
    event.preventDefault();
    setScene(state.currentScene - 1);
  }
});

let pointerStartX = null;

scenePlayer.addEventListener("pointerdown", (event) => {
  pointerStartX = event.clientX;
});

scenePlayer.addEventListener("pointerup", (event) => {
  if (pointerStartX === null) {
    return;
  }

  const deltaX = event.clientX - pointerStartX;

  if (deltaX < -60) {
    goToNextScene();
  } else if (deltaX > 60) {
    setScene(state.currentScene - 1);
  }

  pointerStartX = null;
});

renderScene();
